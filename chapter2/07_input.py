# input is used to take input from the user. It takes string input from the user and returns it as a string.

a = input("Enter your name: ")
print("Hello " + a + "! Welcome to Python programming.")

# int(input()) is used to take integer input from the user. It takes string input from the user, converts it to an integer and returns it as an integer.

b = int(input("Enter your age: "))
print("You are " + str(b) + " years old.")

# float(input()) is used to take floating point number input from the user. It takes string input from the user, converts it to a float and returns it as a float.
c = float(input("Enter your height in meters: "))
print("Your height is " + str(c) + " meters.")