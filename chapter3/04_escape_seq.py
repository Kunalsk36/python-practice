# \n - start new line
text = "Kunal\nKavathekar"
print(text)

# \t - add tab space
text = "Kunal\tKavathekar"
print(text)

# \\ - use to show backslash
text = "Kunal\\Kavathekar"
print(text)

# \' - use to show single quote
text = 'Kunal\'s laptop'
print(text)

# \" - use to show double quote
text = "Kunal said, \"I am learning Python.\""
print(text)

# r - use to show raw string
path = r"C:\Users\Admin\Documents"
print(path)
